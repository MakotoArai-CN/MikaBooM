// CheckAndWarnExpiry 检查并警告过期（用于启动时）
func CheckAndWarnExpiry() {
	if !IsValid() {
		color.Red("╔═══════════════════════════════════════════════╗")
		color.Red("║           ⚠️  版本已过期 ⚠️                    ║")
		color.Red("║   不支持的版本，请更新应用程序                 ║")
		color.Red("║   当前仅支持监控功能                          ║")
		color.Red("╚═══════════════════════════════════════════════╝")
		fmt.Println()
		return
	}

	days := GetDaysUntilExpire()
	if days <= 7 {
		color.Yellow("╔═══════════════════════════════════════════════╗")
		color.Yellow("║           ⚠️  版本即将过期 ⚠️                  ║")
		color.Yellow("║   版本将在 %2d 天后过期                        ║", days)
		color.Yellow("║   请及时更新应用程序                          ║")
		color.Yellow("╚═══════════════════════════════════════════════╝")
		fmt.Println()
	} else if days <= 30 {
		color.New(color.FgYellow).Printf("⚠️  提示: 版本将在 %d 天后过期\n\n", days)
	}
}

