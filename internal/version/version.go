// IsExpiringSoon 检查是否即将过期（30天内）
func IsExpiringSoon() bool {
	if !IsValid() {
		return false
	}
	return GetDaysUntilExpire() <= 30
}

// GetVersionStatus 获取版本状态描述
func GetVersionStatus() string {
	if !IsValid() {
		return "已过期"
	}

	days := GetDaysUntilExpire()
	if days <= 7 {
		return fmt.Sprintf("即将过期 (%d天)", days)
	} else if days <= 30 {
		return fmt.Sprintf("临近过期 (%d天)", days)
	} else {
		return fmt.Sprintf("有效 (%d天)", days)
	}
}

// parseDateTime 解析日期时间字符串
func parseDateTime(dateStr string) (time.Time, error) {
	// 支持多种日期格式
	formats := []string{
		"2006-01-02 15:04:05",
		"2006-01-02T15:04:05",
		"2006-01-02 15:04:05 MST",
		"2006-01-02T15:04:05Z07:00",
	}

	var lastErr error
	for _, format := range formats {
		t, err := time.Parse(format, dateStr)
		if err == nil {
			return t, nil
		}
		lastErr = err
	}

	return time.Time{}, lastErr
}

// getSystemInfo 获取系统信息字符串
func getSystemInfo() string {
	osName := runtime.GOOS
	archName := runtime.GOARCH

	// 美化操作系统名称
	osDisplay := osName
	switch osName {
	case "windows":
		osDisplay = "Windows"
	case "linux":
		osDisplay = "Linux"
	case "darwin":
		osDisplay = "macOS"
	}

	// 美化架构名称
	archDisplay := archName
	switch archName {
	case "amd64":
		archDisplay = "x64"
	case "386":
		archDisplay = "x86"
	case "arm64":
		archDisplay = "ARM64"
	case "arm":
		archDisplay = "ARM"
	}

	return fmt.Sprintf("%s %s", osDisplay, archDisplay)
}

